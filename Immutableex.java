package com.rama.collections;

public final class Immutableex {

		private final int id;
		private final String name;
		
		public Immutableex(int id, String name) {
			this.id = id;
			this.name = name;
		}

		public int getId() {
			return id;
		}

		public String getName() {
			return name;
		}

		

	}

